a = 26
b = 5
# Arithmetic Operators
print(a+b) #Add
print(a-b) #diff
print(a*b) #product
print(a/b) #divide
print(a%b) #modulus
print(a//b) #floor division gives the complete number avoiding decimals
print(a**b) #exponent

#Assignment Operators
# | Operator | Example | Meaning               |
# | -------- | ------- | --------------------- |
# | `=`      | a = 10  | Assign value          |
# | `+=`     | a += 5  | a = a + 5             |
# | `-=`     | a -= 5  | a = a - 5             |
# | `*=`     | a *= 5  | a = a * 5             |
# | `/=`     | a /= 5  | a = a / 5             |
# | `%=`     | a %= 5  | a = a % 5             |
# | `//=`    | a //= 5 | Floor division assign |
# | `**=`    | a **= 2 | Power assign          |

b = 2
print(b)
b += 2
print(b)
b -= 2
print(b)
b *= 2
print(b)
b /= 2
print(b)
b %= 2
print(b)


